package interview1.company1;

public class LearnForLoop {
	
	public static void main(String[] args) {
		
		//to run same code multiple times
		// i want to run 1 to 100
		for(int i = 1; i <= 100; i++ ) {
			System.out.println(i);
		} // or below code for decreasing values
		
		for(int i = 10; i >= 1; i--) {
			System.out.println(i);
		}

		}

}
